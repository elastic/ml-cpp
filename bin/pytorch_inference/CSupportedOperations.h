#ifndef INCLUDED_ml_torch_CSupportedOperations_h
#define INCLUDED_ml_torch_CSupportedOperations_h

#include <string_view>
#include <unordered_set>

namespace ml {
namespace torch {

//! \brief
//! Flat allowlist of TorchScript operations observed across all
//! supported transformer architectures (BERT, RoBERTa, DistilBERT,
//! ELECTRA, MPNet, DeBERTa, BART, DPR, MobileBERT, XLM-RoBERTa).
//!
//! DESCRIPTION:\n
//! Generated by tracing reference HuggingFace models with
//! dev-tools/extract_model_ops/extract_model_ops.py and collecting the union of all
//! operations from the inlined forward() computation graphs.
//!
//! IMPLEMENTATION DECISIONS:\n
//! Stored as a compile-time data structure rather than an external
//! config file to avoid runtime loading failures and to keep the
//! security boundary self-contained.  The list should be regenerated
//! whenever the set of supported architectures changes or when
//! upgrading the PyTorch version.
//!
class CSupportedOperations {
public:
    using TStringViewSet = std::unordered_set<std::string_view>;

    //! Operations explicitly forbidden regardless of the allowlist.
    static const TStringViewSet FORBIDDEN_OPERATIONS;

    //! Union of all TorchScript operations observed in supported architectures.
    static const TStringViewSet ALLOWED_OPERATIONS;
};
}
}

#endif // INCLUDED_ml_torch_CSupportedOperations_h
